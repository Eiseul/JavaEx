public class Q1 {
    public static void main(String[] args) {
        // Q1. while문을 이용해서 1부터 100까지 출력하세요

        int num = 1;                        // 정수선언

        while( num <= 100 ) {               // 반복문 생성 (조건 - 언제까지 반복하라)
            System.out.println(num);        // 출력문 생성 (뭘 출력할 건지 - 숫자)
            num++;                          // 1씩 증가해라
        }
    } 
}

